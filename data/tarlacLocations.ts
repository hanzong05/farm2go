// Helper function to search barangays
export const searchBarangays = (query: string): BarangayData[] => {
  if (!query || query.length < 1) return [];

  const searchTerm = query.toLowerCase();
  return tarlacBarangays.filter(barangay =>
    barangay.name.toLowerCase().includes(searchTerm)
  ).slice(0, 10); // Limit to 10 results
};

// Helper function to search streets within a barangay
export const searchStreets = (barangayName: string, query: string): string[] => {
  if (!query || query.length < 1) return [];

  const barangay = tarlacBarangays.find(b =>
    b.name.toLowerCase() === barangayName.toLowerCase()
  );

  if (!barangay) return [];

  const searchTerm = query.toLowerCase();
  return barangay.streets.filter(street =>
    street.toLowerCase().includes(searchTerm)
  ).slice(0, 10); // Limit to 10 results
};